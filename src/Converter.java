
public class Converter {
	
	public String toUpperCase(String s)
	{
		return s.toUpperCase();
	}
	
	public String toLowerCase(String s)
	{
		return s.toLowerCase();
				
	}
	
	public String toAsk(String s)
	{
		return s+"?";
	}
	
	public String toExamination(String s)
	{
		return s+"!";
	}
}
